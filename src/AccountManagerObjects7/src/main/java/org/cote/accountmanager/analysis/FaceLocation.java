package org.cote.accountmanager.analysis;

public class FaceLocation{
	private int top = 0;
	private int right = 0;
	private int bottom = 0;
	private int left = 0;
	public FaceLocation() {
		
	}

	public int getTop() {
		return top;
	}

	public void setTop(int top) {
		this.top = top;
	}

	public int getRight() {
		return right;
	}

	public void setRight(int right) {
		this.right = right;
	}

	public int getBottom() {
		return bottom;
	}

	public void setBottom(int bottom) {
		this.bottom = bottom;
	}

	public int getLeft() {
		return left;
	}

	public void setLeft(int left) {
		this.left = left;
	}
	
}


